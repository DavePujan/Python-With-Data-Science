# data_Types.py - Python Data Types

# Integer
num = 10

# Float
pi = 3.14

# String
name = "Poojan"

# List
fruits = ["apple", "banana", "mango"]

# Tuple
coordinates = (10, 20)

# Dictionary
student = {"name": "Poojan", "age": 19}

# Set
unique_values = {1, 2, 3, 2}

# Print all types
print(type(num), type(pi), type(name), type(fruits), type(coordinates), type(student), type(unique_values))
